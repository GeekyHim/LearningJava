package PatternPrintingSquares;

public class patternSq {
    public static void main(String[] args) {
        
    
        for(int i = 1; i <=4; i++){ // rows
            for(int j = 1; j<=5;j++){ // columns
                System.out.print("*");
            }
            System.out.println();
        }
    }
}
